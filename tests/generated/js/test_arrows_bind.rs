#[test]
fn test_arrows_bind_js_format_1_3b5ec4c2() -> Result<()> {
    let pretty_printer = PrettyPrinterBuilder::default()
        .parser("js")
        .print_width(80)
        .build()
        .unwrap();
    let formatted = pretty_printer
        .format("a => ({}::b()``[''].c++ && 0 ? 0 : 0);\n(a => b)::c;\na::(b => c);")?;
    assert_eq!(
        formatted,
        "(a) => ({})::b()``[\"\"].c++ && 0 ? 0 : 0;\n((a) => b)::c;\na::((b) => c);"
    );
    Ok(())
}
